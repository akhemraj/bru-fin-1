mintBtn.onclick = async function(){
    const {contractInst, signer} = await getContractInstance();
    const trx = await contractInst.mintByUser(testIPFSURI, {value: mintFees});
	getBalance();
}

async function getContractInstance(){
    await provider.send("eth_requestAccounts", []);
    const signer = provider.getSigner();

	console.log("window.ethereum.networkVersion",window.ethereum.networkVersion);
	if(window.ethereum.networkVersion == '5'){
		selectedContractAddr = goerliContractAddress ;
	}
	else if(window.ethereum.networkVersion == '80001'){
		selectedContractAddr =  contractAddress ;
	}
	console.log(selectedContractAddr)

    const contractInst = new ethers.Contract(
        selectedContractAddr,
        ABI,
        signer
    )

    return {contractInst, signer}
}

async function getBalance(){
    const {contractInst, signer} = await getContractInstance();
    const address = await signer.getAddress();
	console.log(contractInst);
    let tokenBalance = await contractInst.balanceOf(address);
    tokenBalance = tokenBalance.toString();
    console.log(tokenBalance);

    document.querySelector('#tokens').innerText = tokenBalance;
}

transferBtn.onclick = async function(){
    const {contractInst, signer} = await getContractInstance();
    const address = await signer.getAddress();
    const to = document.querySelector('#toAddress').value;
    const tokenId = document.querySelector('#tokenId').value;

    const trx = await contractInst.transferFrom(address, to, tokenId);
    console.log(trx);
}

